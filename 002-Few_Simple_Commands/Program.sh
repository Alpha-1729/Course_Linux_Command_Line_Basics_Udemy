#!/bin/bash
# Few Simple Commands

:'
>>>> Linux commands are case-sensitive.
>>>> For using the cal command in linux.
        sudo apt install ncal
>>>>
>>>>
'

# cal command will show the calendar of the current month.
cal

# To show the calendar of the whole year.
cal -y

# To show the calendar of the a particular year.
cal 2020

# To show the calendar in any year and a month.
cal 1 2020 # Show the calendar of the January 2020.

# To show the previous, current and next month calendar.
cal -3

# Clear the terminal.
clear

# Closing the terminal.
exit

echo "Hello World"