#!/bin/bash
# Alias-2

:'
>>>> Alias commands can help bridge language barriers.
        For example, in German, "date" is "datum".
        You can create an alias for the `date` command as follows:
        alias datum="date"
>>>>
>>>>
>>>>
'

# Useful aliases for Windows users.
alias copy="cp"                    # Aliases `copy` to `cp` command.
copy fileName destinationDirectory # Use `copy` to copy files.

alias dir="ls"    # Aliases `dir` to `ls` command.
alias rename="mv" # Aliases `rename` to `mv` command.
alias md="mkdir"  # Aliases `md` to `mkdir` command.
alias rd="rmdir"  # Aliases `rd` to `rmdir` command.

echo "Hello World"
