#!/bin/bash
# View Files With Cat And Tac

:'
>>>> cat -> catenate (Same like concatenate)
>>>> tac -> Show the content of the file in reverse order.
>>>>
>>>>
'

# To view the file content.
cat fileName

# To view the content of the multiple file.
# It will show the content of first file, second file etc.
cat fileName1 fileName2

#  To show the content of the file in reverse order.
tac fileName

# To view the content of the multiple file in reverse order.
tac fileName1 fileName2

echo "Hello World"